calificacion= int(input("ingresar la calificacion de la persona:"))

if calificacion >=90:
    print("su calificacion se encuentra en el rango A")
elif calificacion >=80:
    print("su calificacion se encuentra en el rango B")
elif calificacion >=70:
    print("su calificacion se encuentra en el rango C")
elif calificacion >=60:
    print("su calificacion se encuentra en el rango D")
else:
    print("su calificacion se encuentra en el rango E")